    private void mostraUsuarios(){
        cbNomeDevolucao.removeAllItems(); // REMOVE OS ITENS DA COMBOBOX PARA QUE NÃO TENHA DUPLICIDADE AO CLICAR NELA
        cbNomeEmprestimo.removeAllItems();
        cbNomeEmprestimo.addItem(null); // INICIA A COMBOBOX COMO VAZIA
        cbNomeDevolucao.addItem(null);
        cbNomeDevolucao.setSelectedIndex(0);
        cbNomeEmprestimo.setSelectedIndex(0);
        
        
        for ( Usuario u : Usuario.listarUsuarios() ){
            
            cbNomeEmprestimo.addItem(u);
            
            // SE O USUÁRIO TIVER MAIS DE UM LIVRO ELE É ADICIONADO A LISTA DE DEVOLUÇÃO
            if ( u.getQtdLivrosEmprestados() > 0 ) {
                cbNomeDevolucao.addItem(u);
            }
        }
    }
    
    private void mostraLivros(){
        cbLivroDevolucao.removeAllItems(); // REMOVE OS ITENS DA COMBOBOX PARA QUE NÃO TENHA DUPLICIDADE AO CLICAR NELA
        cbLivroEmprestimo.removeAllItems();
        cbLivroEmprestimo.addItem(null); // INICIA A COMBOBOX COMO VAZIA
        cbLivroDevolucao.addItem(null);
        cbLivroDevolucao.setSelectedIndex(0);
        cbLivroEmprestimo.setSelectedIndex(0);
        
        for( Livro l : Livro.mostraLivros() ){
            // SE O LIVRO NÃO ESTIVER DISPONIVEL, ADD ELE A SELEÇÃO DE DEVOLUCAO
            if ( l.getDisponivel() == false ){
                cbLivroDevolucao.addItem(l);
            }
            // SE O LIVRO ESTIVER DISPONIVEL, ADD ELE A SELEÇÃO DE EMPRESTIMO
            if( l.getDisponivel() ) {
                cbLivroEmprestimo.addItem(l);
            }
        }
    }
    
    
