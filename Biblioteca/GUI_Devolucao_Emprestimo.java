    private void btnEmprestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmprestarActionPerformed
        // TODO add your handling code here:
        Livro livroSelecionado = (Livro) cbLivroEmprestimo.getSelectedItem(); //PEGA O DADO DA COMBOBOX
        Usuario usuarioSelecionado = (Usuario) cbNomeEmprestimo.getSelectedItem(); //PEGA O DADO DA COMBOBOX
        
        if ( cbNomeEmprestimo.getSelectedItem() == null && cbLivroEmprestimo.getSelectedItem() == null ){
                javax.swing.JOptionPane.showMessageDialog(this, "TODOS OS CAMPOS DEVEM SER SELECIONADOS!");
        } else{
                if ( usuarioSelecionado.getQtdLivrosEmprestados() >= 5 ) {
                    javax.swing.JOptionPane.showMessageDialog(this, "LIMITE DE EMPRÉSTIMO ATINGIDO!!");
                } else{
                        // SE O LIVRO ESTIVER DISPONIVEL ENTRA NA CONDIÇÃO
                        if ( livroSelecionado.getDisponivel()) {
                        livroSelecionado.emprestar();
                        usuarioSelecionado.adicionarLivro(livroSelecionado); //ADICIONA O LIVRO SELECIONADO A LISTA DE LIVROS QUE O USUARIO POSSUI
                        cbLivroEmprestimo.removeItem(livroSelecionado); //REMOVE O ITEM DA SELEÇÃO DE EMPRESTIMO
                        cbLivroDevolucao.addItem(livroSelecionado); // ADD A SELEÇÃO DE DEVOLUÇÃO
                        cbNomeDevolucao.removeAllItems();
                        cbNomeDevolucao.addItem(usuarioSelecionado); // ADD O USUARIO A SELEÇÃO DE DEVOLUÇÃO
                        cbNomeDevolucao.setSelectedIndex(0);
                        javax.swing.JOptionPane.showMessageDialog(this, "LIVRO EMPRESTADO!");
                        cbLivroEmprestimo.setSelectedIndex(0);
                        cbNomeEmprestimo.setSelectedIndex(0);
                    }
                }  
            }
    }//GEN-LAST:event_btnEmprestarActionPerformed

    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        // TODO add your handling code here:
        Livro livroSelecionado = (Livro) cbLivroDevolucao.getSelectedItem(); //PEGA O DADO DA COMBOBOX
        Usuario usuarioSelecionado = (Usuario) cbNomeDevolucao.getSelectedItem(); //PEGA O DADO DA COMBOBOX
        
        if ( cbLivroDevolucao.getSelectedItem() == null || cbNomeDevolucao.getSelectedItem() == null ){
            javax.swing.JOptionPane.showMessageDialog(this, "TODOS OS CAMPOS DEVEM SER SELECIONADOS!");
        } else {
                    // SE O LIVRO NÃO ESTIVER DISPONIVEL ENTRA NA CONDIÇÃO
                if ( livroSelecionado.getDisponivel() == false ) {
                    livroSelecionado.devolver();
                    usuarioSelecionado.getLivrosEmprestados().remove(livroSelecionado);
                    cbLivroDevolucao.removeItem(livroSelecionado); //REMOVE O ITEM DA SELEÇÃO DE DEVOLUÇÃO
                    cbLivroDevolucao.setSelectedIndex(0);
                    cbLivroEmprestimo.addItem(livroSelecionado); // ADD A SELEÇÃO DE DISPONIVEL
                    javax.swing.JOptionPane.showMessageDialog(this, "LIVRO DEVOLVIDO!");
                    cbNomeDevolucao.setSelectedIndex(0);
                    int qtdEmprestada = usuarioSelecionado.getQtdLivrosEmprestados() - 1;
                    usuarioSelecionado.setQtdLivrosEmprestados(qtdEmprestada);
                    

                    if ( usuarioSelecionado.getLivrosEmprestados().isEmpty() ) {
                        cbNomeDevolucao.removeItem(usuarioSelecionado); //REMOVE O USARIO DA SELEÇÃO DE USUARIOS COM LIVROS A EMPRESTAR
                    }

                    Collections.sort(Livro.mostraLivros()); //ORDENA A LISTA POR ORDEM ALFABETICA
                    cbLivroDevolucao.setSelectedIndex(0);
                }
            }
        
        

    }//GEN-LAST:event_btnDevolverActionPerformed

